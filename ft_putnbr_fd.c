#include "libft.h"

void	ft_putnbr_fd(long n, int fd)
{
	char	s;

	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n < 10)
	{
		s = '0' + n;
		write(fd, &s, 1);
	}
	else
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
}
